export function getLangFromUrl(url: URL) {
  // Check URL parameter first (?lang=ja)
  const langParam = url.searchParams.get('lang');
  if (langParam && langParam in ui) {
    return langParam as keyof typeof ui;
  }
  
  // Check path (/ja/...)
  const [, lang] = url.pathname.split('/');
  if (lang in ui) return lang as keyof typeof ui;
  
  // Check localStorage (client-side only)
  if (typeof window !== 'undefined') {
    const stored = localStorage.getItem('preferred-lang');
    if (stored && stored in ui) {
      return stored as keyof typeof ui;
    }
  }
  
  return defaultLang;
}

export function useTranslations(lang: keyof typeof ui) {
  return function t(key: keyof typeof ui[typeof defaultLang]) {
    return ui[lang][key] || ui[defaultLang][key];
  }
}
